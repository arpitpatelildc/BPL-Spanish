class SizeConfigs {
  static   MediaQueryData? _mediaQueryData;
  static  double? screenWidth;
  static  double? screenHeight;
  static  double? defaultSize;
  static  Orientation? orientation;

  void init(BuildContext context) {
    _mediaQueryData = MediaQuery.of(context);
    screenWidth = _mediaQueryData!.size.width;
    screenHeight = _mediaQueryData!.size.height;
    orientation = _mediaQueryData!.orientation;
    defaultSize = orientation == Orientation.landscape
        ? screenHeight! * 0.024
        : screenWidth! * 0.024;
  }
}

double getFont(double size) {
  double defaultsSize = SizeConfigs.defaultSize! * size;
  return (defaultsSize / 10);
}

// Get the proportionate height as per screen size
double getHeight(double inputHeight) {
  double screenHeight = SizeConfigs.screenHeight!;
  // 812 is the layout height that designer use
  return (inputHeight / 812.0) * screenHeight;
}

// Get the proportionate width as per screen size
double getWidth(double inputWidth) {
  double screenWidth = SizeConfigs.screenWidth!;
  // 375 is the layout width that Figma provides
  return (inputWidth / 375.0) * screenWidth;
}

bool isLarge(BuildContext context) {
  return getWidthSize(context) > 375;
}

bool isSmall(BuildContext context) {
  return getWidthSize(context) < 800;
}

bool isMedium(BuildContext context) {
  return getWidthSize(context) > 800 && getWidthSize(context) < 1200;
}

double getWidthSize(BuildContext context) {
  return MediaQuery.of(context).size.width;
}

double getHeightSize(BuildContext context) { 
  return MediaQuery.of(context).size.height;
}
